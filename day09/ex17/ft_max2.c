int		ft_max(int *tab, int length)
{
	int i;

	i = 0;
	while (i < length)
	{
		if (tab[0] < tab[i + 1])
		{
			tab[0] = tab[i + 1];
		}
		i++;
	}
	return (tab[0]);
}
