char	*ft_strcat(char *dest, char *src)
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (dest[a])
	{
		a = a + 1;
	}
	while (src[b])
	{
		dest[a] = src[b];
		a = a + 1;
		b = b + 1;
	}
	dest[a] = '\0';
	return (dest);
}

