int		ft_str_is_numeric(char *str)
{
	int index;

	index = 0;
	while (str[index])
	{
		if (!('0' <= str[index] && str[index] <= '9'))
		{
			return (0);
		}
		index = index + 1;
	}
	return (1);
}
