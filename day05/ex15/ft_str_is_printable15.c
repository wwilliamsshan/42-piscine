int		ft_str_is_printable(char *str)
{
	int index;

	index = 0;
	while (str[index] != '\0')
	{
		if (!(32 <= str[index] && str[index] <= 126))
		{
			return (0);
		}
		index = index + 1;
	}
	return (1);
}
