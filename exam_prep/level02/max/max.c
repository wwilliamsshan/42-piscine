int		max(int *tab, unsigned int len);
{
	unsigned int 	i;
	int				ret;

	i = 0;
	ret = -2147483648;
	if (tab[i])
	{
		while (i < len)
		{
			if (tab[i] > ret)
			{
				ret = tab[i];
			}
			i++;
		}
		return (ret);
	}
	else
	{
		return (0);
	}
}
